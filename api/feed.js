function getProductType(imageLink) {
    if (!imageLink) return '';
    if (/package/i.test(imageLink)) return 'המארזים שלנו';
    const match = imageLink.match(/MNP_(\d+)/i);
    if (!match) return '';
    return MNP_CATEGORIES[match[1]] || '';
}

export default async function handler(req, res) {
    try {
        const { blobs } = await list({ prefix: 'products.json' });
        const products = blobs.length > 0
            ? await fetch(blobs[0].url).then(r => r.json())
            : [];

        const headers = [
            'id', 'title', 'description', 'availability', 'condition',
            'price', 'link', 'image_link', 'brand', 'product_type'
        ];

        const rows = products.map(p => {
            const productType = getProductType(p.image_link || '');
            return headers.map(header => {
                let val = header === 'product_type' ? productType : (p[header] || '');
                if (header === 'image_link') {
                    val = val.toString().replace(/-\d+x\d+(\.\w+)$/, '$1');
                }
                if (header === 'price' && val && !val.toString().includes('ILS')) {
                    val = `${parseFloat(val).toFixed(2)} ILS`;
                }
                val = val.toString().replace(/"/g, '""');
                return `"${val}"`;
            }).join(',');
        });

        const csvContent = [headers.join(','), ...rows].join('\n');

        res.setHeader('Content-Type', 'text/csv; charset=utf-8');
        res.setHeader('Access-Control-Allow-Origin', '*');
        return res.status(200).send(csvContent);
    } catch (err) {
        console.error(err);
        return res.status(500).send('Internal Server Error');
    }
}
